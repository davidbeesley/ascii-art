package com.github.davidbeesley.asciiart.util;

public class Dimension {
    int height;
    int width;

    public Dimension(int height, int width) {
        this.height = height;
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }
}
